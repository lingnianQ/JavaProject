package day04;

/**
 * Switch ...case
 */
public class SwitchDemo {
    public static void main(String[] args) {
        int num =1;
        switch (num){ //byte ,shot ,char ,int
            case 1:
                System.out.println(1);
                break;
            case 2:
                System.out.println(2);
                break;
            case 3:
                System.out.println(3);
                break;
            default:
                System.out.println("default!!");
        }
    }
}
